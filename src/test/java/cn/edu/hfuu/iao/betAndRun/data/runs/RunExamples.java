  /**
   * create a random run
   *
   * @return a random run
   */
  @SuppressWarnings("incomplete-switch")
  public static final Run random() {
    final ThreadLocalRandom random;

    random = ThreadLocalRandom.current();
    switch (random.nextInt(100)) {
      case 0: {
        return Run.getDefault();
      }
      case 1: {
        return RunExamples.artificial_1();
      }
      case 2: {
        return RunExamples.artificial_2();
      }
      case 3: {
        return RunExamples.artificial_3();
      }
      case 4: {
        return RunExamples.artificial_4();
      }
      case 5: {
        return RunExamples.artificial_5();
      }
      case 6: {
        return RunExamples.artificial_6();
      }
      case 7: {
        return RunExamples.artificial_7();
      }
      case 8: {
        return RunExamples.artificial_8();
      }
      case 9: {
        return RunExamples.artificial_9();
      }
      case 10: {
        return RunExamples.fastvcMTX_bio_dmela_mtx_100_0_1_csv();
      }
      case 11: {
        return RunExamples.fastvcMTX_socfb_B_anon_mtx_100_0_1_csv();
      }
    }
    final long[] data;
    int index;
    long lastTime, currentQuality, currentTime;

    data = new long[2 + (random.nextInt(10) * 100)];

    outer: for (;;) {
      index = 0;

      do {
        currentQuality = random.nextLong();
      } while (currentQuality <= (data.length << 1));

      do {
        currentTime = random.nextLong();
      } while ((currentTime <= 0L)
          || (currentTime >= (Long.MAX_VALUE - (data.length * 100))));

      while (index < data.length) {

        lastTime = currentTime++;
        currentTime += random.nextInt(100);
        if ((currentTime <= lastTime) || (currentTime >= Long.MAX_VALUE)) {
          continue outer;
        }

        currentQuality--;
        currentQuality -= random.nextInt(100);
        if (currentQuality <= 0L) {
          continue outer;
        }

        data[index++] = currentTime;
        data[index++] = currentQuality;
      }

      return new _ArrayBackedRun(data);
    }
  }
